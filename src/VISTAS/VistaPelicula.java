 public void BorrarFilaPelicula(){
    
        int a=modelo.getRowCount()-1;
        for( int i = a; i >=0; i-- ){
            modelo.removeRow(i);
        }
    }
  public void CargarDatosPelicula(){
    
        BorrarFilaPelicula();
        
        for (Pelicula c : listaPelicula){
            modelo.addRow(new Object[]{c.getId_pelicula(),c.getNombre(),c.getGenero(),c.getAnio()});
        } 
    }
  public void CabeceraTablaPelicula(){
      ArrayList<Object> columns = new ArrayList<Object>();
      columns.add("id_pelicula");
      columns.add("nombre");
      columns.add("genero");
      columns.add("anio");
       
      for(Object it: columns){
          modelo.addColumn(it);
      }
      tPelicula.setModel(modelo);
      
    }

